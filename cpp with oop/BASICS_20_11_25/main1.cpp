#include <iostream>
using namespace std;

int main(){
    int a, b;
    cout<<"Enter first number:"<<endl;
    cin>>a;

    cout<<"Enter second number:"<<endl;
    cin>>b;

    cout<<"The second number is: "<<a<<endl;
    cout<<"The second number is: "<<b<<endl;

    cout<<"The sum number is: "<<a+b<<endl;
    cout<<"The sum number is: "<<a-b<<endl;
    cout<<"The multiple number is: "<<a*b<<endl;
    cout<<"The divided number is: "<<(float)a/b<<endl;

    return 0;
}